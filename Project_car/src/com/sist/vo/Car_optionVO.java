package com.sist.vo;

public class Car_optionVO {
	private String trim_num, option_name, option_price;

	public String getTrim_num() {
		return trim_num;
	}

	public void setTrim_num(String trim_num) {
		this.trim_num = trim_num;
	}

	public String getOption_name() {
		return option_name;
	}

	public void setOption_name(String option_name) {
		this.option_name = option_name;
	}

	public String getOption_price() {
		return option_price;
	}

	public void setOption_price(String option_price) {
		this.option_price = option_price;
	}
}
